let num = document.querySelector('input#cont')
let lista = document.querySelector('select#flista')
let res = document.querySelector('div#res')
let valores = []

function isNumero(n) {
    if (Number(n) >= 1 && Number(n) <= 100) {
        return true
    } else {
        return false
    }
}

function inLista(n, l) {
    if (l.indexOf(Number(n)) != -1) {
        return true
    } else {
        return false
    }
}

function adicionar() {
    /* Se for um número e não estiver na lista */
    if (isNumero(num.value) && !inLista(num.value, valores)) {
        valores.push(Number(num.value))
        let item = document.createElement('option')
        item.text = `Valor ${num.value} adicionado.`
        lista.appendChild(item)
        res.innerHTML = ''
    } else {
        window.alert('Valor inválido ou já encontrado na lista.')
    }
    num.value = ''
    num.focus()
}

function finalizar() {
    if (valores.length == 0) {
        window.alert('Adicione valores antes de finalizar!')
    } else {
        let ttl = valores.length
        let maior = valores[0]
        let menor = valores[0]
        let soma = 0
        let media = 0
        for (let pos in valores) {
            soma += valores[pos]
            if (valores[pos] > maior)
                maior = valores[pos]
            if (valores[pos] < menor)
                menor = valores[pos]
        }
        media = soma / ttl
        res.innerHTML = ''
        res.innerHTML += `<p>Ao todo, temos ${ttl} números cadastrados na análise.</p>`
        res.innerHTML += `<p>O <strong>menor valor</strong> informado na análise é ${menor} e o <strong>maior valor</strong> é ${maior}. </p>`
        res.innerHTML += `<p>Somando todos os valores da análise, temos ${soma}.`
        res.innerHTML += `<p>A media dos valores da análise é ${media}.`
    }
}